      _model.fromDate = widget.docDocument?.sFDate;
      _model.toDate = _model.toDate;
      setState(() {});
      // get booking data
      _model.returnedBookedData = await queryDocBookedTimeRecordOnce(
        queryBuilder: (docBookedTimeRecord) => docBookedTimeRecord
            .where(
              'doc_id',
              isEqualTo: widget.docDocument?.reference.id,
            )
            .where(
              'date',
              isGreaterThanOrEqualTo: _model.fromDate,
            )
            .where(
              'date',
              isLessThanOrEqualTo: _model.toDate,
            )
            .where(
              'status_cde',
              isEqualTo: 3,
            ),
      );
      // get booking count
      _model.totBookingCount = valueOrDefault<int>(
        _model.returnedBookedData?.length,
        0,
      );
      setState(() {});
      _model.fromDate = widget.docDocument?.sFDate;
      _model.toDate = widget.docDocument?.sToDate;
      _model.subscriptionFee = valueOrDefault<int>(
        widget.docDocument?.sCost,
        0,
      );
      _model.feePerBook = valueOrDefault<int>(
        widget.docDocument?.sFee,
        0,
      );
      _model.totFees = valueOrDefault<int>(
        valueOrDefault<int>(
              _model.totBookingCount,
              0,
            ) *
            FFAppState().bookingFee,
        0,
      );
      _model.totCost = valueOrDefault<int>(
        valueOrDefault<int>(
              _model.totFees,
              0,
            ) +
            valueOrDefault<int>(
              FFAppState().subscrptionFee,
              0,
            ),
        0,
      );
      _model.remainingDays = valueOrDefault<int>(
        functions.dateDifference(_model.fromDate, _model.toDate),
        0,
      );
      setState(() {});
    });

    WidgetsBinding.instance.addPostFrameCallback((_) => setState(() {}));
  }

